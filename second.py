# Type casting
x = 1
y = 2.0
z = "3"

y = int(y)

print(x)
print(y)
print(z)

# User input
name = input("What is your name?: ")
age = int(input("How old is your age?: "))
height = float(input("How tall are you?: "))

print("Your name is " + name)
print("You are " + str(age) + " years old")
print("You are " + str(height) + "cm tall")